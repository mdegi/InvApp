    public boolean entriesComplete() {
        boolean entriesComplete = true;
        if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected()) {
            entriesComplete = false;   
        } else {
            if (!StdFun.checkStandardDate(jFormattedTextField1.getText())) {
                entriesComplete = false;
            } else {
                if ((jFormattedTextField2.getText() == null) && (StdFun.getIntValue(jFormattedTextField2.getText()) != 0)) {
                    entriesComplete = false;
                } else {
                    if (jTextField1.getText() == null) {
                        jTextField1.setText(CASH_ACCOUNT);                        
                        jTextField2.setText(null);
                        jTextField3.setText(null);
                        jTextField4.setText(null);
                    } 
                }
            }
        }
        return entriesComplete;
    }
    
    public void fillRecord() {
        headerRecord.initVars();        
        headerRecord.setTransType(TransType.getTransType(jLabel1.getText()));
        headerRecord.setTransDate(StdFun.getSQLDate(jFormattedTextField1.getText()));
        headerRecord.setReference(StdFun.getIntValue(jFormattedTextField2.getText()));        
        headerRecord.setAccNumber(jLabel6.getText());
        headerRecord.setAccName(jTextField1.getText());
        headerRecord.setAccAddLine1(jTextField2.getText());
        headerRecord.setAccAddLine2(jTextField3.getText());
        headerRecord.setAccVatNum(jTextField4.getText());        
    }
    
