    private void setTransactionName(ActionEvent evt) {
        jRadioButton1.setSelected(false);
        jRadioButton2.setSelected(false);
        jRadioButton3.setSelected(false);

        jLabel1.setText(TransType.valueOf(evt.getActionCommand()).getDisplayName());
        if (evt.getActionCommand().equals(TransType.INVOICE.toString())) {
            jRadioButton1.setSelected(true);
        } else if (evt.getActionCommand().equals(TransType.CASH_SALE.toString())) {
            jRadioButton2.setSelected(true);
        }
        else if (evt.getActionCommand().equals(TransType.QUOTATION.toString())) {
            jRadioButton3.setSelected(true);
        } else {
            jLabel1.setText("Please Select");
        }        
    }

